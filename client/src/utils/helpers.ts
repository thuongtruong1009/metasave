export const applyDrag = (arr: any, dragResult: any) => {
  const { removedIndex, addedIndex, payload } = dragResult;
  if (removedIndex === null && addedIndex === null) return arr;
  const result = [...arr];
  let itemToAdd = payload;
  if (removedIndex !== null) {
    itemToAdd = result.splice(removedIndex, 1)[0];
  }
  if (addedIndex !== null) {
    result.splice(addedIndex, 0, itemToAdd);
  }
  return result;
};

export const generateItems = (count: any, creator: any) => {
  const result = [];
  for (let i = 0; i < count; i++) {
    result.push(creator(i));
  }
  return result;
};

export const generateWords = (n: number) => {
  let words = lorem.split(" ");
  let index = Math.floor(Math.random() * words.length);
  let result = new Array();
  while (result.length < n) {
    if (index < words.length) result.push(words[index]);
    else {
      // if im over of ther array start from 0
      index = 0;
      result.push(words[index]);
    }
    index++;
  }
  return result.join(" ");
};

